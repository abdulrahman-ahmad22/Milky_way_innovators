exports.handler = async (event) => {
  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, body: JSON.stringify({ error: 'Method Not Allowed' }) };
  }

  const apiKey = process.env.GEMINI_API_KEY;
  if (!apiKey) {
    return {
      statusCode: 500,
      body: JSON.stringify({ error: 'الخدمة غير مهيّأة: مفتاح GEMINI_API_KEY غير موجود في إعدادات البيئة.' }),
    };
  }

  let history;
  try {
    const parsed = JSON.parse(event.body || '{}');
    history = Array.isArray(parsed.history) ? parsed.history : null;
  } catch (_) {
    history = null;
  }
  if (!history || history.length === 0) {
    return { statusCode: 400, body: JSON.stringify({ error: 'طلب غير صالح.' }) };
  }

  // حدّ بسيط للحماية من إساءة الاستخدام
  if (history.length > 40) history = history.slice(-40);

  const url =
    `https://generativelanguage.googleapis.com/v1beta/models/${GEMINI_MODEL}:generateContent?key=${apiKey}`;

  try {
    const res = await fetch(url, {
      method: 'POST',
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({
        systemInstruction: { parts: [{ text: SYSTEM_PROMPT }] },
        contents: history,
        generationConfig: {
          temperature: 0.8,
          topP: 0.95,
          maxOutputTokens: 800,
          thinkingConfig: { thinkingBudget: 0 },
        },
      }),
    });

    if (!res.ok) {
      const detail = await res.text().catch(() => '');
      return {
        statusCode: 502,
        body: JSON.stringify({ error: 'تعذّر الاتصال بالخدمة الذكية.', detail: detail.slice(0, 300) }),
      };
    }

    const data = await res.json();
    const reply =
      data?.candidates?.[0]?.content?.parts?.map((p) => p.text).join('').trim() || '';

    if (!reply) {
      return { statusCode: 502, body: JSON.stringify({ error: 'رد فارغ من الخدمة.' }) };
    }

    return {
      statusCode: 200,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ reply }),
    };
  } catch (err) {
    return {
      statusCode: 500,
      body: JSON.stringify({ error: 'خطأ غير متوقع في الخادم.', detail: String(err && err.message || err) }),
    };
  }
};
